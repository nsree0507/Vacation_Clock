export const getPlacesByState = (stateId) => {
  return placesData.filter(place => place.stateId === stateId)
}

// Get places by category
export const getPlacesByCategory = (category) => {
  return placesData.filter(place => place.primaryCategory === category)
}

// Get single place by slug or id
export const getPlaceBySlug = (slug) => {
  const normalizedSlug = typeof slug === 'string' ? slug.toLowerCase() : ''
  return placesData.find((place) =>
    place.slug === slug ||
    place.id === slug ||
    place._id === slug ||
    String(place.slug).toLowerCase() === normalizedSlug ||
    String(place.id).toLowerCase() === normalizedSlug ||
    String(place._id).toLowerCase() === normalizedSlug
  )
}

